    private void ViewRowToForm() {
        if (dong < 0 || dong >= tbKhachHang.getRowCount()) {
            JOptionPane.showMessageDialog(this, "Chưa chọn dòng nào hoặc dòng không hợp lệ!");
            return;
        }

        KhachHangTable model = (KhachHangTable) tbKhachHang.getModel();
        KhachHang kh = model.getKhachHangAt(dong); // Phương thức getKhachHangAt cần được định nghĩa trong KhachHangTable để trả về KhachHang tại một dòng cụ thể
        txtMaKH.setText(kh.getMakh());
        txtTenKH.setText(kh.getTenKh());
        txtDiaChi.setText(kh.getDiaChi());
        txtTuoi.setText(Integer.toString(kh.getTuoi()));
        txtSDT.setText(kh.getSoDt());
        if (kh.getGioiTinh().equals("Nam")) {
            rdNam.setSelected(true);
            rdNu.setSelected(false);
        } else {
            rdNam.setSelected(false);
            rdNu.setSelected(true);
        }
    }

    private void findByPhone() {
        String phoneNumber = JOptionPane.showInputDialog(this, "Nhập số điện thoại khách hàng: ");
        if (phoneNumber == null || phoneNumber.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Số điện thoại không được bỏ trống.");
            return;
        }
        phoneNumber = phoneNumber.trim();
        int vitri = -1;
        try {
            // Lấy mô hình bảng hiện tại
            KhachHangTable model = (KhachHangTable) tbKhachHang.getModel();
            for (int i = 0; i < model.getRowCount(); i++) {
                String phone = (String) model.getValueAt(i, 5); // cột 5 là số điện thoại khách hàng
                if (phone.equalsIgnoreCase(phoneNumber)) {
                    vitri = i;
                    break;
                }
            }
            if (vitri >= 0) {
                JOptionPane.showMessageDialog(this, "OK: " + phoneNumber);
                tbKhachHang.setRowSelectionInterval(vitri, vitri);
                dong = vitri;
                ViewRowToForm();
            } else {
                JOptionPane.showMessageDialog(this, "Không tìm thấy số điện thoại " + phoneNumber);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void findByID() {
        String ma = JOptionPane.showInputDialog(this, "Nhập mã khách hàng: ");
        if (ma == null) {
            // User canceled or closed the dialog, so we don't show any message.
            return;
        } else if (ma.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã khách hàng không được bỏ trống.");
            return;
        }
        ma = ma.trim();
        int vitri = -1;
        try {

            KhachHangTable model = (KhachHangTable) tbKhachHang.getModel();
            for (int i = 0; i < model.getRowCount(); i++) {
                String maKH = (String) model.getValueAt(i, 0); // Cột 0 là mã khách hàng
                if (maKH.equalsIgnoreCase(ma)) {
                    vitri = i;
                    break;
                }
            }
            if (vitri >= 0) {
                JOptionPane.showMessageDialog(this, "OK: " + ma);
                tbKhachHang.setRowSelectionInterval(vitri, vitri);
                dong = vitri;
                ViewRowToForm();
            } else {
                JOptionPane.showMessageDialog(this, "Không tìm thấy mã " + ma);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void findByNameOrPlaceOrSex() {
        String ten = txtTenKH.getText().trim();
        String diachi = txtDiaChi.getText().trim();
        String tuoiStr = txtTuoi.getText().trim();
        String gioitinh = "";
        if (rdNam.isSelected()) {
            gioitinh = "Nam";
        } else if (rdNu.isSelected()) {
            gioitinh = "Nu";
        }
        //kiểm tra đầu vào
        if (ten.isEmpty() && diachi.isEmpty() && tuoiStr.isEmpty() && gioitinh.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập ít nhất một tiêu chí tìm kiếm.");
            return;
        }
        ArrayList<KhachHang> lstFind = new ArrayList<>();

        for (KhachHang kh : dsKH) {
            boolean match = true;
            if (!ten.isEmpty() && !kh.getTenKh().equalsIgnoreCase(ten)) {
                match = false;
            }
            if (!diachi.isEmpty() && !kh.getDiaChi().equalsIgnoreCase(diachi)) {
                match = false;
            }
            if (!tuoiStr.isEmpty()) {
                try {
                    int tuoi = Integer.parseInt(tuoiStr);
                    if (kh.getTuoi() != tuoi) {
                        match = false;
                    }
                } catch (NumberFormatException e) {
                    match = false;
                }
            }
            if (!gioitinh.isEmpty() && !kh.getGioiTinh().equalsIgnoreCase(gioitinh)) {
                match = false;
            }
            if (match) {
                lstFind.add(kh);
            }
        }

        if (lstFind.size() > 0) {
            JOptionPane.showMessageDialog(this, "OK: " + lstFind.size() + " khách hàng được tìm thấy.");
            loadKHTable(lstFind);

//            tbKhachHang.setRowSelectionInterval(0, 0);
//            dong = 0;
//            ViewRowToForm();
        } else {
            JOptionPane.showMessageDialog(this, "NOT OK: Không tìm thấy khách hàng nào.");
            loadKHTable(new ArrayList<>());
        }
    }

