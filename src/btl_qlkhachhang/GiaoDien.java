    // Chức năng xóa Khách hàng (Dũng)
    public void deleteCustomer(int index) throws DeleteCustomerException, IOException {
        if (index == -1) {
            throw new DeleteCustomerException("Vui lòng chọn khách hàng cần xóa!");
        }

        KhachHang kh = dsKH.get(index);

        // Kiểm tra xem khách hàng có đơn hàng nào không
        for (DonHang dh : dsDH) {
            if (dh.getMakh().equals(kh.getMakh())) {
                throw new DeleteCustomerException("Không thể xóa khách hàng đã có đơn hàng!");
            }
        }

        int luaChon = JOptionPane.showConfirmDialog(
                rootPane,
                "Bạn có chắc chắn muốn xóa khách hàng này?",
                "Xác nhận xóa",
                JOptionPane.YES_NO_OPTION
        );

        // Khi người quản trị chọn Đồng ý xóa
        if (luaChon == JOptionPane.YES_OPTION) {
            dsKH.remove(index);
            FileUtils.ghiObjectVaoFile("khachhang.txt", dsKH);
            loadKHTable(dsKH);
            clearKH();
            JOptionPane.showMessageDialog(rootPane, "Xóa thành công");
        }
    }

